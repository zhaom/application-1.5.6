package com.babeeta.butterfly.subscription.entity;

public class Binding {
	private String aid;
	private String cid;
	private String did;

	public String getAid() {
		return aid;
	}

	public String getCid() {
		return cid;
	}

	public String getDid() {
		return did;
	}

	public void setAid(String aid) {
		this.aid = aid;
	}

	public void setCid(String cid) {
		this.cid = cid;
	}

	public void setDid(String did) {
		this.did = did;
	}

}
